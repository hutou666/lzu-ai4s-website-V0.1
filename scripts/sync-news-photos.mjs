/**
 * 按发布日期将新闻配图从素材文件夹同步到 public，并生成 news.generated.ts。
 *
 * 素材路径：素材/新闻动态 照片/
 * 命名约定：
 *   YYYY-MM-DD.jpg          → 封面（列表卡片 + 详情页头图）
 *   YYYY-MM-DD-01.jpg       → 正文插图（可选，按序号追加）
 *   YYYY-MM-DD/01.jpg       → 子文件夹模式：01 为封面，02+ 为插图
 *
 * 输出路径：public/assets/news/
 * 生成文件：src/content/media/news.generated.ts
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const MATERIALS_DIR = path.join(root, "素材");
const SOURCE_FOLDER = "新闻动态 照片";
const SRC_DIR = path.join(MATERIALS_DIR, SOURCE_FOLDER);
const PUBLIC_DIR = path.join(root, "public", "assets", "news");
const OUT_FILE = path.join(root, "src", "content", "media", "news.generated.ts");
const NEWS_FILE = path.join(root, "src", "content", "news.ts");

const IMAGE_EXT = new Set([".jpg", ".jpeg", ".png", ".webp", ".gif"]);
const DATE_RE = /^(\d{4}-\d{2}-\d{2})$/;
const FLAT_NAME_RE = /^(\d{4}-\d{2}-\d{2})(?:-(\d{1,2}))?$/;

function listImageFiles(dir) {
  if (!fs.existsSync(dir)) return [];
  return fs
    .readdirSync(dir, { withFileTypes: true })
    .filter((e) => e.isFile() && IMAGE_EXT.has(path.extname(e.name).toLowerCase()))
    .map((e) => e.name)
    .sort((a, b) => a.localeCompare(b, "zh-CN", { numeric: true }));
}

function parseNewsEntries() {
  const src = fs.readFileSync(NEWS_FILE, "utf8");
  const slugs = [...src.matchAll(/slug:\s*"([^"]+)"/g)].map((m) => m[1]);
  const dates = [...src.matchAll(/date:\s*"(\d{4}-\d{2}-\d{2})"/g)].map((m) => m[1]);
  const titles = [...src.matchAll(/title:\s*"([^"]+)"/g)].map((m) => m[1]);

  return dates.map((date, i) => ({
    date,
    slug: slugs[i] ?? `news-${i}`,
    title: titles[i] ?? date,
  }));
}

function addAsset(byDate, date, sortIndex, relativePath) {
  if (!byDate.has(date)) {
    byDate.set(date, []);
  }
  byDate.get(date).push({ sortIndex, relativePath });
}

function collectSourceAssets() {
  const byDate = new Map();

  if (!fs.existsSync(SRC_DIR)) {
    return byDate;
  }

  for (const file of listImageFiles(SRC_DIR)) {
    const stem = path.basename(file, path.extname(file));
    const match = stem.match(FLAT_NAME_RE);
    if (!match) {
      continue;
    }
    const sortIndex = match[2] ? Number.parseInt(match[2], 10) : 0;
    addAsset(byDate, match[1], sortIndex, file);
  }

  for (const entry of fs.readdirSync(SRC_DIR, { withFileTypes: true })) {
    if (!entry.isDirectory() || !DATE_RE.test(entry.name)) continue;

    const files = listImageFiles(path.join(SRC_DIR, entry.name));
    files.forEach((file, index) => {
      const stem = path.basename(file, path.extname(file));
      const numbered = stem.match(/^(\d{1,2})$/);
      const sortIndex = numbered ? Number.parseInt(numbered[1], 10) : index + 1;
      addAsset(byDate, entry.name, sortIndex, path.join(entry.name, file));
    });
  }

  for (const items of byDate.values()) {
    items.sort((a, b) => a.sortIndex - b.sortIndex);
  }

  return byDate;
}

function clearDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
  for (const existing of fs.readdirSync(dir)) {
    const full = path.join(dir, existing);
    if (fs.statSync(full).isFile()) fs.unlinkSync(full);
  }
}

function writeGeneratedTs(mediaByDate) {
  const header = `// Auto-generated by scripts/sync-news-photos.mjs — do not edit manually
// Source: 素材/${SOURCE_FOLDER}/
// Match news items by date field in src/content/news.ts

export interface NewsMediaEntry {
  date: string;
  cover: string;
  images: string[];
}

export const newsMediaByDate: Record<string, NewsMediaEntry> = ${JSON.stringify(mediaByDate, null, 2)};
`;
  fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
  fs.writeFileSync(OUT_FILE, header, "utf8");
}

/**
 * @param {{ quiet?: boolean }} options
 */
export function syncNewsPhotos(options = {}) {
  const { quiet = false } = options;

  if (!fs.existsSync(SRC_DIR)) {
    fs.mkdirSync(SRC_DIR, { recursive: true });
    fs.writeFileSync(path.join(SRC_DIR, ".gitkeep"), "", "utf8");
    if (!quiet) {
      console.warn(`Created empty source folder: 素材/${SOURCE_FOLDER}/`);
    }
  }

  const newsEntries = parseNewsEntries();
  const newsDates = new Set(newsEntries.map((e) => e.date));
  const sourceByDate = collectSourceAssets();

  if (!quiet) {
    console.log("Syncing news photos...");
    console.log(`  Source: 素材/${SOURCE_FOLDER}/`);
    console.log(`  News entries: ${newsEntries.length}`);
  }

  clearDir(PUBLIC_DIR);

  const mediaByDate = {};
  let copied = 0;

  for (const [date, assets] of sourceByDate) {
    const images = [];

    for (const asset of assets) {
      const ext = path.extname(asset.relativePath).toLowerCase();
      let outName = `${date}${asset.sortIndex > 0 ? `-${String(asset.sortIndex).padStart(2, "0")}` : ""}${ext}`;
      if (asset.sortIndex === 0 && assets.length === 1) {
        outName = `${date}${ext}`;
      } else if (asset.sortIndex > 0) {
        outName = `${date}-${String(asset.sortIndex).padStart(2, "0")}${ext}`;
      } else {
        outName = `${date}${ext}`;
      }

      const srcPath = path.join(SRC_DIR, asset.relativePath);
      const dstPath = path.join(PUBLIC_DIR, outName);
      fs.copyFileSync(srcPath, dstPath);

      const url = `/assets/news/${outName}`;
      images.push(url);
      copied += 1;

      if (!quiet) {
        console.log(`  ${asset.relativePath} -> ${outName}`);
      }
    }

    if (images.length > 0) {
      mediaByDate[date] = {
        date,
        cover: images[0],
        images,
      };
    }
  }

  writeGeneratedTs(mediaByDate);

  const missingPhotos = newsEntries.filter((e) => !mediaByDate[e.date]);
  const orphanPhotos = [...sourceByDate.keys()].filter((date) => !newsDates.has(date));

  if (!quiet) {
    console.log(`\nSynced ${copied} image(s) for ${Object.keys(mediaByDate).length} date(s).`);
    console.log(`Wrote src/content/media/news.generated.ts`);

    if (missingPhotos.length > 0) {
      console.warn(`\nWarning: ${missingPhotos.length} news item(s) without photos:`);
      for (const item of missingPhotos) {
        console.warn(`  ${item.date}  ${item.title}`);
      }
    }

    if (orphanPhotos.length > 0) {
      console.warn(`\nWarning: ${orphanPhotos.length} photo date(s) not in news.ts:`);
      for (const date of orphanPhotos) {
        console.warn(`  ${date}`);
      }
    }

    console.log("\nDone. Add YYYY-MM-DD.jpg to 素材/新闻动态 照片/ then run: npm run sync:news-photos");
  } else if (copied > 0) {
    console.log(`[sync:news-photos] ${copied} image(s) for ${Object.keys(mediaByDate).length} date(s)`);
  }

  return {
    copied,
    matchedDates: Object.keys(mediaByDate).length,
    missingPhotos: missingPhotos.map((e) => e.date),
    orphanPhotos,
  };
}

function main() {
  const quiet = process.argv.includes("--quiet");
  try {
    syncNewsPhotos({ quiet });
  } catch (e) {
    console.error(e.message);
    process.exit(1);
  }
}

const isMain = process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url);
if (isMain) main();
