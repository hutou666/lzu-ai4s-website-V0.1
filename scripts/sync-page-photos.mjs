import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { PAGE_PHOTO_SETS } from "./page-photos.config.mjs";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const MATERIALS_DIR = path.join(root, "素材");
const PUBLIC_PHOTOS_DIR = path.join(root, "public", "assets", "page-photos");
const MEDIA_OUT_DIR = path.join(root, "src", "content", "media");

const IMAGE_EXT = new Set([".jpg", ".jpeg", ".png", ".webp", ".gif"]);

function captionFromFilename(filename) {
  return path.basename(filename, path.extname(filename));
}

function safeFilename(name) {
  return name.replace(/[<>:"/\\|?*]/g, "").trim() || "photo";
}

function listImages(dir) {
  if (!fs.existsSync(dir)) return [];
  return fs
    .readdirSync(dir)
    .filter((f) => IMAGE_EXT.has(path.extname(f).toLowerCase()))
    .sort((a, b) => a.localeCompare(b, "zh-CN", { numeric: true }));
}

function clearDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
  for (const existing of fs.readdirSync(dir)) {
    const full = path.join(dir, existing);
    if (fs.statSync(full).isFile()) fs.unlinkSync(full);
  }
}

function copyGallery(srcDir, dstDir, publicUrlPrefix) {
  const files = listImages(srcDir);
  const gallery = [];

  for (const file of files) {
    const ext = path.extname(file).toLowerCase();
    const caption = captionFromFilename(file);
    let outName = `${safeFilename(caption)}${ext}`;
    let counter = 1;
    while (fs.existsSync(path.join(dstDir, outName))) {
      outName = `${safeFilename(caption)}-${counter}${ext}`;
      counter += 1;
    }

    fs.copyFileSync(path.join(srcDir, file), path.join(dstDir, outName));
    gallery.push({
      src: `${publicUrlPrefix}/${outName}`,
      caption,
    });
    console.log(`  ${file} -> ${outName}`);
  }

  return gallery;
}

function copySlots(srcDir, dstDir, publicUrlPrefix, slots) {
  const files = listImages(srcDir);
  const sources = {};

  for (let i = 0; i < slots.length; i++) {
    const slot = slots[i];
    const file = files[i];
    if (!file) {
      sources[slot] = "";
      continue;
    }

    const ext = path.extname(file).toLowerCase();
    const outName = `${String(i + 1).padStart(2, "0")}${ext}`;
    fs.copyFileSync(path.join(srcDir, file), path.join(dstDir, outName));
    sources[slot] = `${publicUrlPrefix}/${outName}`;
    console.log(`  ${file} -> ${slot} (${outName})`);
  }

  if (files.length > slots.length) {
    console.warn(`  Warning: ${files.length} file(s) but only ${slots.length} slot(s); extras ignored.`);
  }
  if (files.length < slots.length && files.length > 0) {
    console.warn(`  Warning: ${files.length} file(s) for ${slots.length} slot(s); missing slots stay empty.`);
  }

  return sources;
}

function writeGalleryTs({ exportName, galleryTypeName, gallery, set, generator }) {
  const header = `// Auto-generated by ${generator} — do not edit manually
// Page: ${set.label} (${set.id})
// Source: 素材/${set.sourceFolder}/
`;
  if (gallery.length === 0) {
    return `${header}export interface ${galleryTypeName} {
  src: string;
  caption: string;
}

export const ${exportName}: ${galleryTypeName}[] = [];
`;
  }
  return `${header}export interface ${galleryTypeName} {
  src: string;
  caption: string;
}

export const ${exportName}: ${galleryTypeName}[] = ${JSON.stringify(gallery, null, 2)};
`;
}

function writeSlotsTs({ exportName, sources, set, generator }) {
  const header = `// Auto-generated by ${generator} — do not edit manually
// Page: ${set.label} (${set.id})
// Source: 素材/${set.sourceFolder}/
// Slots: ${set.slots.join(", ")}
`;
  return `${header}export const ${exportName} = ${JSON.stringify(sources, null, 2)} as const;

export type ${exportName.charAt(0).toUpperCase() + exportName.slice(1).replace(/Src$/, "Slot")} = keyof typeof ${exportName};
`;
}

function countTimelineNodes(timelineFile) {
  const full = path.join(root, timelineFile);
  if (!fs.existsSync(full)) return null;
  const src = fs.readFileSync(full, "utf8");
  const block = src.match(/overviewGalleryTimeline[^=]*=\s*\[([\s\S]*?)\];/);
  if (!block) return null;
  return (block[1].match(/\{\s*date:/g) ?? []).length;
}

function syncOne(set) {
  const srcDir = path.join(MATERIALS_DIR, set.sourceFolder);
  const dstDir = path.join(PUBLIC_PHOTOS_DIR, set.id);
  const publicUrlPrefix = `/assets/page-photos/${set.id}`;
  const outFile = path.join(MEDIA_OUT_DIR, set.outputFile);
  const generator = "scripts/sync-page-photos.mjs";

  console.log(`\n[${set.id}] ${set.label}`);

  fs.mkdirSync(MATERIALS_DIR, { recursive: true });
  if (!fs.existsSync(srcDir)) {
    fs.mkdirSync(srcDir, { recursive: true });
    fs.writeFileSync(path.join(srcDir, ".gitkeep"), "", "utf8");
    console.warn(`  Created empty source folder: 素材/${set.sourceFolder}/`);
  }

  clearDir(dstDir);

  let ts;
  if (set.mode === "gallery") {
    const gallery = fs.existsSync(srcDir) ? copyGallery(srcDir, dstDir, publicUrlPrefix) : [];
    ts = writeGalleryTs({
      exportName: set.exportName,
      galleryTypeName: set.galleryTypeName,
      gallery,
      set,
      generator,
    });
    console.log(`  Synced ${gallery.length} image(s) -> public/assets/page-photos/${set.id}/`);

    if (set.timelineFile) {
      const timelineCount = countTimelineNodes(set.timelineFile);
      if (timelineCount != null && gallery.length > 0 && gallery.length !== timelineCount) {
        console.warn(
          `  Warning: ${gallery.length} photo(s) but ${timelineCount} timeline node(s) in ${set.timelineFile}`
        );
      }
    }
  } else if (set.mode === "slots") {
    const sources = fs.existsSync(srcDir)
      ? copySlots(srcDir, dstDir, publicUrlPrefix, set.slots)
      : Object.fromEntries(set.slots.map((s) => [s, ""]));
    ts = writeSlotsTs({ exportName: set.exportName, sources, set, generator });
    const filled = Object.values(sources).filter(Boolean).length;
    console.log(`  Synced ${filled}/${set.slots.length} slot(s) -> public/assets/page-photos/${set.id}/`);
  }

  fs.mkdirSync(MEDIA_OUT_DIR, { recursive: true });
  fs.writeFileSync(outFile, ts, "utf8");
  console.log(`  Wrote src/content/media/${set.outputFile}`);
}

const only = process.argv.slice(2)[0];
const sets = only ? PAGE_PHOTO_SETS.filter((s) => s.id === only) : PAGE_PHOTO_SETS;

if (only && sets.length === 0) {
  console.error(`Unknown page id: ${only}`);
  console.error(`Available: ${PAGE_PHOTO_SETS.map((s) => s.id).join(", ")}`);
  process.exit(1);
}

console.log("Syncing page photos...");
for (const set of sets) syncOne(set);
console.log("\nDone. Add images to 素材/<页面文件夹>/ then run: npm run sync:photos");
